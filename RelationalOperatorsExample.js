// Greater than - `>`
7 > 4; // true
2 > 10; // false
100 > 100; // false

// Less than - `<`
7 < 4; // false
2 < 10; // true
100 < 100; // false

// Greater than or equal to - `>=`
7 >= 4; // true
2 >= 10; // false
100 >= 100; // true

// Less than or equal to - `<=`
7 <= 4; // false
2 <= 10; // true
100 <= 100; // true

// Not equal - `!==` (opposite of ===)
7 !== 4; // true
2 !== 10; // true
100 !== 100; // false
